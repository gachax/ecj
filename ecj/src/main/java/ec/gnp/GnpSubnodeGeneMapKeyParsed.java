package ec.gnp;

public class GnpSubnodeGeneMapKeyParsed {

    private int nodeId;
    private int subnodeId;

    public GnpSubnodeGeneMapKeyParsed(int nodeId, int subnodeId) {
        this.nodeId = nodeId;
        this.subnodeId = subnodeId;
    }

    public int getNodeId() {
        return nodeId;
    }

    public int getSubnodeId() {
        return subnodeId;
    }

}
