package ec.gnp;

public class GnpBranchGeneMapKeyParsed {

    private int nodeId;
    private int branchId;

    public GnpBranchGeneMapKeyParsed(int nodeId, int branchId) {
        this.nodeId = nodeId;
        this.branchId = branchId;
    }

    public int getNodeId() {
        return nodeId;
    }

    public int getBranchId() {
        return branchId;
    }

}
